  private static String joinFigshareTags(JsonNode detail) {
    if (detail == null) {
      return "";
    }
    List<String> values = new ArrayList<>();
    JsonNode tags = detail.path("tags");
    if (tags.isArray()) {
      for (JsonNode tag : tags) {
        String value = tag.asText("").trim();
        if (!value.isBlank()) {
          values.add(value);
        }
      }
    }
    JsonNode categories = detail.path("categories");
    if (categories.isArray()) {
      for (JsonNode category : categories) {
        String value = category.path("title").asText("").trim();
        if (!value.isBlank()) {
          values.add(value);
        }
      }
    }
    return String.join(" ", values);
  }

