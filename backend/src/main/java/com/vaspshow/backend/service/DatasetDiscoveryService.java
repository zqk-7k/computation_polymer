  private static String normalizeDecision(String value) {
    String decision = value == null ? "" : value.trim().toUpperCase(Locale.ROOT);
    if (decision.isBlank()) {
      return "REVIEW";
    }
    if (!List.of("CANDIDATE", "REVIEW", "APPROVED", "REJECTED", "ARCHIVED").contains(decision)) {
      throw new ApiException(HttpStatus.BAD_REQUEST, "候选状态仅支持 CANDIDATE、REVIEW、APPROVED、REJECTED、ARCHIVED");
    }
    return decision;
  }

