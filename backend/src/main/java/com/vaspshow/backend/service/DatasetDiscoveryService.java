  private void persistConfig(String operator) {
    try (Connection connection = openConnection();
         PreparedStatement statement = connection.prepareStatement("""
             MERGE INTO DISCOVERY_CONFIG (ID, CONFIG_JSON, UPDATED_AT, UPDATED_BY)
             KEY (ID) VALUES (1, ?, ?, ?)
             """)) {
      statement.setString(1, objectMapper.writeValueAsString(snapshotConfig()));
      statement.setString(2, Instant.now().toString());
      statement.setString(3, limit(operator, 120));
      statement.executeUpdate();
    } catch (Exception ex) {
      throw new ApiException(HttpStatus.INTERNAL_SERVER_ERROR, "保存发现配置失败: " + ex.getMessage());
    }
  }

  private void loadAndApplyConfigOverrides() {
    try (Connection connection = openConnection();
         PreparedStatement statement = connection.prepareStatement(
             "SELECT CONFIG_JSON FROM DISCOVERY_CONFIG WHERE ID = 1");
         ResultSet rs = statement.executeQuery()) {
      if (rs.next()) {
        String json = rs.getString("CONFIG_JSON");
        if (json != null && !json.isBlank()) {
          applyConfigOverrides(objectMapper.readValue(json, DiscoveryConfigUpdateRequest.class));
        }
      }
    } catch (Exception ignored) {
      // Persisted overrides are best-effort; fall back to yml defaults on any error.
    }
  }

  private static int clamp(int value, int min, int max) {
    return Math.max(min, Math.min(max, value));
  }

  private static List<String> sanitizeQueries(List<String> queries) {
    List<String> cleaned = new ArrayList<>();
    for (String query : queries) {
      if (query == null) {
        continue;
      }
      String trimmed = query.trim();
      if (!trimmed.isBlank() && trimmed.length() <= 200) {
        cleaned.add(trimmed);
        if (cleaned.size() >= 20) {
          break;
        }
      }
    }
    return cleaned.isEmpty()
        ? new ArrayList<>(List.of("computational materials dataset DFT energy force"))
        : cleaned;
  }

