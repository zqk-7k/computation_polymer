  public DiscoveryRunResponse runDiscovery(String authorization) {
    authService.requireDatasetReviewAccess(authorization);
    String operator = authService.me(authorization).username();
    if (!running.compareAndSet(false, true)) {
      return new DiscoveryRunResponse(
          "discover-in-progress",
          Instant.now().toString(),
          "",
          "RUNNING",
          0, 0, 0, 0, 0,
          "已有自动发现任务正在执行，请稍后查看运行记录与候选列表。",
          List.of()
      );
    }
    String runId = "discover-" + UUID.randomUUID();
    String startedAt = Instant.now().toString();
    DiscoveryRunResponse runningResponse = new DiscoveryRunResponse(
        runId,
        startedAt,
        "",
        "RUNNING",
        0, 0, 0, 0, 0,
        "自动发现任务已启动，后台正在检索多源（DataCite / Zenodo / NOMAD）并自动分诊候选。",
        List.of()
    );
    saveRun(runningResponse, operator);
    discoveryExecutor.submit(() -> {
      try {
        runDiscoveryInternal(operator, runId, startedAt);
      } finally {
        running.set(false);
      }
    });
    return runningResponse;
  }

