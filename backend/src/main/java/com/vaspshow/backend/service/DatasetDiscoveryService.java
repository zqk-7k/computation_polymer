  private DiscoveryRunResponse runDiscoveryInternal(String operator, String runId, String startedAt) {
    int discovered = 0;
    int inserted = 0;
    int updated = 0;
    int autoPromoted = 0;
    int autoArchived = 0;
    String status = "SUCCESS";
    String message = "自动发现完成。";
    List<DiscoverySourceStat> sourceStats = new ArrayList<>();
    try {
      DiscoverAllResult discovery = discoverAll();
      sourceStats = discovery.stats();
      List<CandidateDraft> drafts = discovery.drafts();
      discovered = drafts.size();
      boolean validateEnabled = properties.getDiscovery().getValidate().isEnabled();
      for (CandidateDraft draft : drafts) {
        ValidationReport report = validateEnabled ? validationService.validate(toCandidate(draft)) : null;
        UpsertResult result = upsertCandidate(draft, report);
        if (result.inserted()) {
          inserted++;
        } else if (result.updated()) {
          updated++;
        }
      }
      TriageResult triage = triageCandidates(operator);
      autoPromoted = triage.promoted();
      autoArchived = triage.archived();
      boolean allFailed = !sourceStats.isEmpty() && sourceStats.stream().allMatch(s -> "ERROR".equals(s.status()));
      if (allFailed) {
        status = "FAILED";
        message = "自动发现失败：所有数据源均未成功返回（" + summarizeFailures(sourceStats) + "）。";
      } else if (drafts.isEmpty() && autoPromoted == 0 && autoArchived == 0) {
        status = sourceStats.stream().anyMatch(s -> "ERROR".equals(s.status())) ? "PARTIAL" : "SUCCESS";
        message = "未发现新候选；请稍后重试或调整关键词。" + sourceSuffix(sourceStats);
      } else {
        status = sourceStats.stream().anyMatch(s -> "ERROR".equals(s.status())) ? "PARTIAL" : "SUCCESS";
        message = String.format(
            "自动发现完成：发现 %d，新增 %d，更新 %d，自动转入 %d，自动归档 %d。%s",
            discovered, inserted, updated, autoPromoted, autoArchived, sourceSuffix(sourceStats));
      }
    } catch (Exception ex) {
      status = "FAILED";
      message = "自动发现失败: " + ex.getMessage();
    }
    String finishedAt = Instant.now().toString();
    DiscoveryRunResponse run = new DiscoveryRunResponse(
        runId, startedAt, finishedAt, status, discovered, inserted, updated,
        autoPromoted, autoArchived, message, sourceStats);
    saveRun(run, operator);
    return run;
  }

  private DiscoverAllResult discoverAll() {
    IntakeProperties.Discovery cfg = properties.getDiscovery();
    record SourceJob(String name, boolean enabled, DraftSupplier supplier) {
    }
    List<SourceJob> jobs = List.of(
        new SourceJob("DataCite", cfg.isDatacite(), this::discoverFromDataCite),
        new SourceJob("Zenodo", cfg.isZenodo(), this::discoverFromZenodo),
        new SourceJob("Figshare", cfg.isFigshare(), this::discoverFromFigshare),
        new SourceJob("Dryad", cfg.isDryad(), this::discoverFromDryad),
        new SourceJob("OpenAIRE", cfg.isOpenaire(), this::discoverFromOpenAire),
        new SourceJob("NOMAD", cfg.isNomad(), this::discoverFromNomad));

    // Run every source concurrently so a whole run takes about as long as the slowest
    // source rather than the sum of them all (a few sources sit near their timeouts).
    ExecutorService pool = Executors.newFixedThreadPool(jobs.size(), runnable -> {
      Thread thread = new Thread(runnable, "vasp-discovery-source");
      thread.setDaemon(true);
      return thread;
    });
    List<SourceResult> results = new ArrayList<>();
    try {
      List<Callable<SourceResult>> tasks = new ArrayList<>();
      for (SourceJob job : jobs) {
        tasks.add(() -> runSourceJob(job.name(), job.enabled(), job.supplier()));
      }
      List<Future<SourceResult>> futures = pool.invokeAll(tasks, 150, TimeUnit.SECONDS);
      for (int i = 0; i < futures.size(); i++) {
        try {
          results.add(futures.get(i).get());
        } catch (Exception ex) {
          results.add(new SourceResult(
              new DiscoverySourceStat(jobs.get(i).name(), 0, "ERROR", "超时或中断"), List.of()));
        }
      }
    } catch (InterruptedException ex) {
      Thread.currentThread().interrupt();
    } finally {
      pool.shutdownNow();
    }

    // Merge single-threaded with cross-source dedup, preserving source priority order.
    List<CandidateDraft> drafts = new ArrayList<>();
    Set<String> seen = new LinkedHashSet<>();
    List<DiscoverySourceStat> stats = new ArrayList<>();
    for (SourceResult result : results) {
      int added = collect(drafts, seen, result.drafts());
      DiscoverySourceStat stat = result.stat();
      if ("OK".equals(stat.status())) {
        stats.add(new DiscoverySourceStat(stat.source(), stat.found(), "OK",
            "命中 " + stat.found() + "，去重后新增 " + added));
      } else {
        stats.add(stat);
      }
    }
    return new DiscoverAllResult(drafts, stats);
  }

  /**
   * Run a single source connector and capture its outcome (drafts + stat) instead of
   * letting an error vanish silently. A failing source no longer looks like an empty one.
   */
  private SourceResult runSourceJob(String name, boolean enabled, DraftSupplier supplier) {
    if (!enabled) {
      return new SourceResult(new DiscoverySourceStat(name, 0, "DISABLED", "数据源已关闭"), List.of());
    }
    try {
      List<CandidateDraft> found = supplier.get();
      String status = found.isEmpty() ? "EMPTY" : "OK";
      String detail = found.isEmpty() ? "本轮无返回" : "命中 " + found.size();
      return new SourceResult(new DiscoverySourceStat(name, found.size(), status, detail), found);
    } catch (Exception ex) {
      String reason = ex.getMessage() == null ? ex.getClass().getSimpleName() : ex.getMessage();
      return new SourceResult(new DiscoverySourceStat(name, 0, "ERROR", limit(reason, 200)), List.of());
    }
  }

  private static int collect(List<CandidateDraft> target, Set<String> seen, List<CandidateDraft> incoming) {
    int added = 0;
    for (CandidateDraft draft : incoming) {
      if (seen.add(draft.uniqueKey())) {
        target.add(draft);
        added++;
      }
    }
    return added;
  }

  private static String sourceSuffix(List<DiscoverySourceStat> stats) {
    if (stats == null || stats.isEmpty()) {
      return "";
    }
    List<String> parts = new ArrayList<>();
    for (DiscoverySourceStat stat : stats) {
      parts.add(stat.source() + ":" + stat.status() + "(" + stat.found() + ")");
    }
    return "来源 " + String.join(" ", parts) + "。";
  }

  private static String summarizeFailures(List<DiscoverySourceStat> stats) {
    List<String> parts = new ArrayList<>();
    for (DiscoverySourceStat stat : stats) {
      if ("ERROR".equals(stat.status())) {
        parts.add(stat.source() + ": " + stat.detail());
      }
    }
    return String.join("; ", parts);
  }

  private List<CandidateDraft> discoverFromZenodo() throws Exception {
    IntakeProperties.Discovery cfg = properties.getDiscovery();
    List<String> queries = cfg.getQueries() == null || cfg.getQueries().isEmpty()
        ? List.of("computational materials dataset DFT energy force")
        : cfg.getQueries();
    int pageSize = Math.max(1, Math.min(cfg.getMaxResultsPerQuery(), 50));
    List<CandidateDraft> drafts = new ArrayList<>();
    Set<String> seen = new LinkedHashSet<>();
    for (String query : queries) {
      if (query == null || query.isBlank()) {
        continue;
      }
      String url = "https://zenodo.org/api/records?type=dataset&sort=mostrecent&size=" + pageSize
          + "&q=" + URLEncoder.encode(query, StandardCharsets.UTF_8);
      HttpRequest request = HttpRequest.newBuilder(URI.create(url))
          .GET()
          .timeout(Duration.ofSeconds(16))
          .header("User-Agent", userAgent())
          .header("Accept", "application/json")
          .build();
      HttpResponse<String> response = httpClient.send(request, HttpResponse.BodyHandlers.ofString(StandardCharsets.UTF_8));
      if (response.statusCode() < 200 || response.statusCode() >= 300) {
        continue;
      }
      JsonNode hits = objectMapper.readTree(response.body()).path("hits").path("hits");
      if (!hits.isArray()) {
        continue;
      }
      for (JsonNode item : hits) {
        CandidateDraft draft = draftFromZenodo(item);
        if (draft == null || !seen.add(draft.uniqueKey())) {
          continue;
        }
        drafts.add(draft);
      }
    }
    return drafts;
  }

  private CandidateDraft draftFromZenodo(JsonNode item) {
    String doi = text(item, "doi");
    JsonNode metadata = item.path("metadata");
    String title = text(metadata, "title");
    if (title.isBlank() && doi.isBlank()) {
      return null;
    }
    String description = stripHtml(text(metadata, "description"));
    String license = zenodoLicense(metadata.path("license"));
    JsonNode links = item.path("links");
    String landing = firstNonBlank(
        text(links, "self_html"),
        text(links, "html"),
        links.path("doi").asText(""));

    String fileName = "";
    String dataUrl = "";
    String scale = "待下载清点";
    JsonNode files = item.path("files");
    if (files.isArray() && !files.isEmpty()) {
      JsonNode file = files.get(0);
      fileName = firstNonBlank(text(file, "key"), text(file, "filename"));
      dataUrl = firstNonBlank(
          file.path("links").path("self").asText(""),
          file.path("links").path("download").asText(""));
      long size = file.path("size").asLong(file.path("filesize").asLong(0L));
      if (size > 0) {
        scale = size + " bytes";
      }
    }
    if (dataUrl.isBlank()) {
      dataUrl = landing;
    }
    String paperUrl = !doi.isBlank() ? "https://doi.org/" + doi : landing;
    String combined = (title + " " + description + " " + fileName + " " + dataUrl).toLowerCase(Locale.ROOT);
    String format = detectFormat(fileName, "");
    String fields = detectFields(combined);
    String method = inferMethod(combined);
    PreflightProbe probe = new PreflightProbe(format, scale, fields, parserPlan(format, combined));
    int relevance = relevanceOf(combined, probe.format(), probe.fields(), method);
    int score = scoreCandidate(doi, dataUrl, license, probe, relevance);
    String recommendation = recommendation(title, doi, dataUrl, license, probe, relevance, score);
    // Prefer Zenodo's concept DOI so different versions of one dataset collapse to a single candidate.
    String conceptDoi = text(item, "conceptdoi");
    String uniqueKey = normalizeKey(!conceptDoi.isBlank() ? conceptDoi : (!doi.isBlank() ? doi : dataUrl));
    return new CandidateDraft(
        "Zenodo",
        title,
        doi,
        paperUrl,
        dataUrl,
        "Zenodo",
        license.isBlank() ? "待确认" : license,
        probe.format(),
        probe.scale(),
        method,
        probe.fields(),
        probe.parserPlan(),
        score,
        relevance,
        recommendation,
        uniqueKey
    );
  }

  private List<CandidateDraft> discoverFromFigshare() throws Exception {
    IntakeProperties.Discovery cfg = properties.getDiscovery();
    List<String> queries = cfg.getQueries() == null || cfg.getQueries().isEmpty()
        ? List.of("computational materials dataset DFT energy force")
        : cfg.getQueries();
    int pageSize = Math.max(1, Math.min(cfg.getMaxResultsPerQuery(), 25));
    List<CandidateDraft> drafts = new ArrayList<>();
    Set<String> seen = new LinkedHashSet<>();
    int lastErrorStatus = 0;
    String lastErrorDetail = "";
    for (String query : queries) {
      if (query == null || query.isBlank()) {
        continue;
      }
      ObjectNode payload = objectMapper.createObjectNode();
      payload.put("search_for", query);
      payload.put("page_size", pageSize);
      payload.put("item_type", 3); // 3 = dataset
      payload.put("order", "published_date");
      payload.put("order_direction", "desc");
      HttpRequest request = HttpRequest.newBuilder(URI.create("https://api.figshare.com/v2/articles/search"))
          .POST(HttpRequest.BodyPublishers.ofString(objectMapper.writeValueAsString(payload), StandardCharsets.UTF_8))
          .timeout(Duration.ofSeconds(16))
          .header("User-Agent", userAgent())
          .header("Content-Type", "application/json")
          .header("Accept", "application/json")
          .build();
      HttpResponse<String> response = httpClient.send(request, HttpResponse.BodyHandlers.ofString(StandardCharsets.UTF_8));
      if (response.statusCode() < 200 || response.statusCode() >= 300) {
        lastErrorStatus = response.statusCode();
        lastErrorDetail = limit(response.body(), 160);
        continue;
      }
      JsonNode list = objectMapper.readTree(response.body());
      if (!list.isArray()) {
        lastErrorStatus = -1;
        lastErrorDetail = "响应非数组";
        continue;
      }
      for (JsonNode summary : list) {
        long id = summary.path("id").asLong(0L);
        if (id <= 0) {
          continue;
        }
        CandidateDraft draft = draftFromFigshare(fetchFigshareDetail(id), summary);
        if (draft == null || !seen.add(draft.uniqueKey())) {
          continue;
        }
        drafts.add(draft);
      }
    }
    if (drafts.isEmpty() && lastErrorStatus != 0) {
      throw new IllegalStateException("Figshare HTTP " + lastErrorStatus
          + (lastErrorDetail.isBlank() ? "" : " · " + lastErrorDetail));
    }
    return drafts;
  }

  private JsonNode fetchFigshareDetail(long id) {
    try {
      HttpRequest request = HttpRequest.newBuilder(URI.create("https://api.figshare.com/v2/articles/" + id))
          .GET()
          .timeout(Duration.ofSeconds(12))
          .header("User-Agent", userAgent())
          .header("Accept", "application/json")
          .build();
      HttpResponse<String> response = httpClient.send(request, HttpResponse.BodyHandlers.ofString(StandardCharsets.UTF_8));
      if (response.statusCode() < 200 || response.statusCode() >= 300) {
        return null;
      }
      return objectMapper.readTree(response.body());
    } catch (Exception ex) {
      return null; // Detail is best-effort; fall back to the search summary.
    }
  }

