  private List<CandidateDraft> discoverFromDataCite() throws Exception {
    IntakeProperties.Discovery cfg = properties.getDiscovery();
    List<String> queries = cfg.getQueries() == null || cfg.getQueries().isEmpty()
        ? List.of("computational materials dataset DFT energy force")
        : cfg.getQueries();
    int pageSize = Math.max(1, Math.min(cfg.getMaxResultsPerQuery(), 50));

    // 1) Fetch all DataCite items (fast sequential GETs), de-duplicating raw items by
    //    DOI/URL so the expensive per-item HEAD probe never runs twice for one dataset.
    List<JsonNode> items = new ArrayList<>();
    Set<String> rawSeen = new LinkedHashSet<>();
    for (String query : queries) {
      if (query == null || query.isBlank()) {
        continue;
      }
      String url = "https://api.datacite.org/dois?query="
          + URLEncoder.encode(query, StandardCharsets.UTF_8)
          + "&page[size]=" + pageSize;
      HttpRequest request = HttpRequest.newBuilder(URI.create(url))
          .GET()
          .timeout(Duration.ofSeconds(16))
          .header("User-Agent", userAgent())
          .build();
      HttpResponse<String> response = httpClient.send(request, HttpResponse.BodyHandlers.ofString(StandardCharsets.UTF_8));
      if (response.statusCode() < 200 || response.statusCode() >= 300) {
        continue;
      }
      JsonNode data = objectMapper.readTree(response.body()).path("data");
      if (!data.isArray()) {
        continue;
      }
      for (JsonNode item : data) {
        JsonNode attributes = item.path("attributes");
        String key = normalizeKey(firstNonBlank(
            text(attributes, "doi"),
            chooseDataUrl(attributes, text(attributes, "url"), text(attributes, "doi"))));
        if (key.isBlank() || rawSeen.add(key)) {
          items.add(item);
        }
      }
    }

    // 2) Probe + build drafts concurrently; reuse stored probes for already-known keys.
    Map<String, String[]> resolved = loadResolvedProbes();
    List<CandidateDraft> built = probeDataCiteConcurrently(items, resolved);

    // 3) Final de-dup by uniqueKey.
    List<CandidateDraft> drafts = new ArrayList<>();
    Set<String> seen = new LinkedHashSet<>();
    for (CandidateDraft draft : built) {
      if (draft != null && seen.add(draft.uniqueKey())) {
        drafts.add(draft);
      }
    }
    return drafts;
  }

  /**
   * Resolve drafts for DataCite items in parallel. The per-item HTTP HEAD/Range probe
   * dominates a discovery run; a small bounded pool turns minutes of sequential probing
   * into seconds while keeping the whole batch time-boxed.
   */
  private List<CandidateDraft> probeDataCiteConcurrently(List<JsonNode> items, Map<String, String[]> resolved) {
    if (items.isEmpty()) {
      return List.of();
    }
    int threads = Math.min(8, items.size());
    ExecutorService pool = Executors.newFixedThreadPool(threads, runnable -> {
      Thread thread = new Thread(runnable, "vasp-datacite-probe");
      thread.setDaemon(true);
      return thread;
    });
    try {
      List<Callable<CandidateDraft>> tasks = new ArrayList<>();
      for (JsonNode item : items) {
        tasks.add(() -> draftFromDataCite(item, resolved));
      }
      List<CandidateDraft> results = new ArrayList<>();
      for (Future<CandidateDraft> future : pool.invokeAll(tasks, 90, TimeUnit.SECONDS)) {
        try {
          results.add(future.get());
        } catch (Exception ex) {
          results.add(null); // A single probe failure/timeout must not drop the batch.
        }
      }
      return results;
    } catch (InterruptedException ex) {
      Thread.currentThread().interrupt();
      return List.of();
    } finally {
      pool.shutdownNow();
    }
  }

  /**
   * Load uniqueKey -> {format, scale, fields} for candidates whose link was already
   * measured in a previous run (scale carries a byte count), so we can skip re-probing.
   */
  private Map<String, String[]> loadResolvedProbes() {
    Map<String, String[]> map = new HashMap<>();
    try (Connection connection = openConnection();
         PreparedStatement statement = connection.prepareStatement(
             "SELECT UNIQUE_KEY, DATA_FORMAT, DATA_SCALE, DETECTED_FIELDS FROM DISCOVERY_CANDIDATES");
         ResultSet rs = statement.executeQuery()) {
      while (rs.next()) {
        String scale = rs.getString("DATA_SCALE");
        if (scale != null && scale.contains("bytes")) {
          map.put(rs.getString("UNIQUE_KEY"),
              new String[] {rs.getString("DATA_FORMAT"), scale, rs.getString("DETECTED_FIELDS")});
        }
      }
    } catch (SQLException ignored) {
      // Skip-cache is a pure optimization; on error we just probe everything.
    }
    return map;
  }

  private CandidateDraft draftFromDataCite(JsonNode item, Map<String, String[]> resolved) {
    JsonNode attributes = item.path("attributes");
    String doi = text(attributes, "doi");
    String title = firstTitle(attributes.path("titles"));
    String url = text(attributes, "url");
    if (title.isBlank() || (doi.isBlank() && url.isBlank())) {
      return null;
    }
    String publisher = text(attributes, "publisher");
    String repository = inferRepository(publisher, url);
    String description = firstDescription(attributes.path("descriptions"));
    String license = firstRights(attributes.path("rightsList"));
    String dataUrl = chooseDataUrl(attributes, url, doi);
    String paperUrl = doi.isBlank() ? url : "https://doi.org/" + doi;
    String uniqueKey = normalizeKey(!doi.isBlank() ? doi : dataUrl);

    // DataCite often ships native metadata we can use before any HTTP probing.
    String nativeFormats = joinArray(attributes.path("formats"));
    String nativeSize = firstArrayValue(attributes.path("sizes"));
    String subjects = joinSubjects(attributes.path("subjects"));

    String combined = (title + " " + description + " " + dataUrl + " " + publisher + " "
        + subjects + " " + nativeFormats).toLowerCase(Locale.ROOT);

    String format;
    String scale;
    String fields;
    String[] cached = resolved == null ? null : resolved.get(uniqueKey);
    if (cached != null) {
      // Already probed in an earlier run — reuse the stored result, skip the HTTP HEAD.
      format = cached[0];
      scale = cached[1];
      fields = (cached[2] == null || cached[2].isBlank()) ? detectFields(combined) : cached[2];
    } else {
      PreflightProbe rawProbe = probeDataUrl(dataUrl, combined);
      format = "待识别".equals(rawProbe.format()) && !nativeFormats.isBlank()
          ? detectFormat("", nativeFormats)
          : rawProbe.format();
      boolean scaleUnknown = "待下载清点".equals(rawProbe.scale()) || "链接预检失败".equals(rawProbe.scale());
      scale = scaleUnknown && !nativeSize.isBlank() ? nativeSize : rawProbe.scale();
      fields = rawProbe.fields();
    }
    PreflightProbe probe = new PreflightProbe(format, scale, fields, parserPlan(format, combined));

    String method = inferMethod(combined);
    int relevance = relevanceOf(combined, probe.format(), probe.fields(), method);
    int score = scoreCandidate(doi, dataUrl, license, probe, relevance);
    String recommendation = recommendation(title, doi, dataUrl, license, probe, relevance, score);
    return new CandidateDraft(
        "DataCite",
        title,
        doi,
        paperUrl,
        dataUrl,
        repository,
        license.isBlank() ? "待确认" : license,
        probe.format(),
        probe.scale(),
        method,
        probe.fields(),
        probe.parserPlan(),
        score,
        relevance,
        recommendation,
        uniqueKey
    );
  }

  private PreflightProbe probeDataUrl(String dataUrl, String combinedText) {
    String format = detectFormat(dataUrl, "");
    String scale = "待下载清点";
    String fields = detectFields(combinedText);
    String parserPlan = parserPlan(format, combinedText);
    if (dataUrl.isBlank() || !dataUrl.startsWith("http")) {
      return new PreflightProbe(format, scale, fields, parserPlan);
    }
    if (!OutboundUrlGuard.isSafe(dataUrl)) {
      // SSRF guard: never dereference a discovered URL that resolves to an internal host.
      return new PreflightProbe(format, "链接预检失败（安全拦截）", fields, parserPlan);
    }
    try {
      HttpRequest head = HttpRequest.newBuilder(URI.create(dataUrl))
          .method("HEAD", HttpRequest.BodyPublishers.noBody())
          .timeout(Duration.ofSeconds(8))
          .header("User-Agent", userAgent())
          .build();
      HttpResponse<Void> headResponse = httpClient.send(head, HttpResponse.BodyHandlers.discarding());
      String contentType = headResponse.headers().firstValue("content-type").orElse("");
      String contentLength = headResponse.headers().firstValue("content-length").orElse("");
      if (!contentLength.isBlank()) {
        scale = contentLength + " bytes";
      }
      format = detectFormat(dataUrl, contentType);
      if ("待识别".equals(format) && headResponse.statusCode() >= 200 && headResponse.statusCode() < 400) {
        HttpRequest sample = HttpRequest.newBuilder(URI.create(dataUrl))
            .GET()
            .timeout(Duration.ofSeconds(10))
            .header("Range", "bytes=0-" + (SAMPLE_BYTES - 1))
            .header("User-Agent", userAgent())
            .build();
        HttpResponse<byte[]> sampleResponse = httpClient.send(sample, HttpResponse.BodyHandlers.ofByteArray());
        if (sampleResponse.statusCode() >= 200 && sampleResponse.statusCode() < 400) {
          String sampleText = new String(sampleResponse.body(), 0, Math.min(sampleResponse.body().length, 4096), StandardCharsets.UTF_8);
          format = detectFormat(dataUrl, contentType + " " + sampleText);
          fields = detectFields(combinedText + " " + sampleText);
        }
      }
      return new PreflightProbe(format, scale, fields, parserPlan(format, combinedText));
    } catch (Exception ex) {
      return new PreflightProbe(format, "链接预检失败", fields, parserPlan);
    }
  }

  private UpsertResult upsertCandidate(CandidateDraft draft, ValidationReport report) throws SQLException {
    String now = Instant.now().toString();
    String vStatus = report == null ? "" : report.status();
    String vJson = serializeReport(report);
    String vAt = report == null ? "" : report.validatedAt();
    try (Connection connection = openConnection();
         PreparedStatement query = connection.prepareStatement(
             "SELECT ID, STATUS FROM DISCOVERY_CANDIDATES WHERE UNIQUE_KEY = ?")) {
      query.setString(1, draft.uniqueKey());
      try (ResultSet rs = query.executeQuery()) {
        if (rs.next()) {
          long id = rs.getLong("ID");
          try (PreparedStatement update = connection.prepareStatement("""
              UPDATE DISCOVERY_CANDIDATES
              SET SOURCE = ?, TITLE = ?, DOI = ?, PAPER_URL = ?, DATA_URL = ?, REPOSITORY = ?,
                  LICENSE_TEXT = ?, DATA_FORMAT = ?, DATA_SCALE = ?, METHOD_TEXT = ?,
                  DETECTED_FIELDS = ?, PARSER_PLAN = ?, SCORE = ?, RECOMMENDATION = ?, RELEVANCE = ?,
                  VALIDATION_STATUS = ?, VALIDATION_JSON = ?, VALIDATED_AT = ?, UPDATED_AT = ?
              WHERE ID = ?
              """)) {
            bindDraft(update, draft);
            update.setString(16, vStatus);
            update.setString(17, vJson);
            update.setString(18, vAt);
            update.setString(19, now);
            update.setLong(20, id);
            update.executeUpdate();
          }
          return new UpsertResult(false, true);
        }
      }
      try (PreparedStatement insert = connection.prepareStatement("""
          INSERT INTO DISCOVERY_CANDIDATES (
            SOURCE, TITLE, DOI, PAPER_URL, DATA_URL, REPOSITORY, LICENSE_TEXT,
            DATA_FORMAT, DATA_SCALE, METHOD_TEXT, DETECTED_FIELDS, PARSER_PLAN,
            SCORE, RECOMMENDATION, RELEVANCE, STATUS, REVIEW_NOTE, DISCOVERED_AT, UPDATED_AT, UNIQUE_KEY,
            VALIDATION_STATUS, VALIDATION_JSON, VALIDATED_AT
          ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 'CANDIDATE', '', ?, ?, ?, ?, ?, ?)
          """)) {
        bindDraft(insert, draft);
        insert.setString(16, now);
        insert.setString(17, now);
        insert.setString(18, draft.uniqueKey());
        insert.setString(19, vStatus);
        insert.setString(20, vJson);
        insert.setString(21, vAt);
        insert.executeUpdate();
      }
      return new UpsertResult(true, false);
    }
  }

