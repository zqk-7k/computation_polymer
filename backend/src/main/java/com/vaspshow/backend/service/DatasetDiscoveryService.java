  /** Super-admin: apply editable config overrides at runtime and persist them. */
  public DiscoveryConfigResponse updateConfig(String authorization, DiscoveryConfigUpdateRequest request) {
    authService.requireSuperAdmin(authorization);
    String operator = authService.me(authorization).username();
    if (request != null) {
      applyConfigOverrides(request);
    }
    persistConfig(operator);
    return getConfig(authorization);
  }

  private void applyConfigOverrides(DiscoveryConfigUpdateRequest r) {
    IntakeProperties.Discovery d = properties.getDiscovery();
    if (r.enabled() != null) d.setEnabled(r.enabled());
    if (r.queries() != null) d.setQueries(sanitizeQueries(r.queries()));
    if (r.maxResultsPerQuery() != null) d.setMaxResultsPerQuery(clamp(r.maxResultsPerQuery(), 1, 50));
    if (r.datacite() != null) d.setDatacite(r.datacite());
    if (r.zenodo() != null) d.setZenodo(r.zenodo());
    if (r.figshare() != null) d.setFigshare(r.figshare());
    if (r.nomad() != null) d.setNomad(r.nomad());
    if (r.dryad() != null) d.setDryad(r.dryad());
    if (r.openaire() != null) d.setOpenaire(r.openaire());
    if (r.validateEnabled() != null) d.getValidate().setEnabled(r.validateEnabled());
    IntakeProperties.AutoPromote ap = d.getAutoPromote();
    if (r.autoPromoteEnabled() != null) ap.setEnabled(r.autoPromoteEnabled());
    if (r.autoPromoteMinScore() != null) ap.setMinScore(clamp(r.autoPromoteMinScore(), 0, 100));
    if (r.autoPromoteMinRelevance() != null) ap.setMinRelevance(clamp(r.autoPromoteMinRelevance(), 0, 100));
    if (r.autoPromoteRequireLicense() != null) ap.setRequireLicense(r.autoPromoteRequireLicense());
    if (r.autoPromoteRequireValidationPass() != null) ap.setRequireValidationPass(r.autoPromoteRequireValidationPass());
    if (r.autoPromoteMaxPerRun() != null) ap.setMaxPerRun(clamp(r.autoPromoteMaxPerRun(), 1, 100));
    IntakeProperties.AutoArchive aa = d.getAutoArchive();
    if (r.autoArchiveEnabled() != null) aa.setEnabled(r.autoArchiveEnabled());
    if (r.autoArchiveMaxScore() != null) aa.setMaxScore(clamp(r.autoArchiveMaxScore(), 0, 100));
    IntakeProperties.AutoAdapt ad = d.getAutoAdapt();
    if (r.autoAdaptEnabled() != null) ad.setEnabled(r.autoAdaptEnabled());
    if (r.autoAdaptMaxDownloadMb() != null) ad.setMaxDownloadMb(clamp(r.autoAdaptMaxDownloadMb(), 1, 4096));
    if (r.autoAdaptSampleMb() != null) ad.setSampleMb(clamp(r.autoAdaptSampleMb(), 1, 256));
  }

