  @Scheduled(cron = "${vasp.intake.discovery.cron:0 15 3 ? * MON}")
  public void weeklyDiscovery() {
    if (!properties.getDiscovery().isEnabled()) {
      return;
    }
    if (!running.compareAndSet(false, true)) {
      return; // A discovery run is already in progress.
    }
    try {
      runDiscoveryInternal("system");
    } catch (Exception ignored) {
      // Scheduled discovery must not break the web application.
    } finally {
      running.set(false);
    }
  }

