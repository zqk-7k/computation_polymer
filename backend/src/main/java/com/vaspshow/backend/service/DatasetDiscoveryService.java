  private static String firstTitle(JsonNode titles) {
    if (titles.isArray() && !titles.isEmpty()) {
      return text(titles.get(0), "title");
    }
    return "";
  }

  private static String firstDescription(JsonNode descriptions) {
    if (descriptions.isArray() && !descriptions.isEmpty()) {
      return text(descriptions.get(0), "description");
    }
    return "";
  }

  private static String firstRights(JsonNode rights) {
    if (rights.isArray() && !rights.isEmpty()) {
      String title = text(rights.get(0), "rights");
      String uri = text(rights.get(0), "rightsUri");
      return title.isBlank() ? uri : title;
    }
    return "";
  }

  private static String joinArray(JsonNode array) {
    if (!array.isArray() || array.isEmpty()) {
      return "";
    }
    List<String> values = new ArrayList<>();
    for (JsonNode node : array) {
      String value = node.asText("").trim();
      if (!value.isBlank()) {
        values.add(value);
      }
    }
    return String.join(" ", values);
  }

  private static String firstArrayValue(JsonNode array) {
    if (array.isArray() && !array.isEmpty()) {
      return array.get(0).asText("").trim();
    }
    return "";
  }

  private static String joinSubjects(JsonNode subjects) {
    if (!subjects.isArray() || subjects.isEmpty()) {
      return "";
    }
    List<String> values = new ArrayList<>();
    for (JsonNode node : subjects) {
      String value = node.path("subject").asText("").trim();
      if (!value.isBlank()) {
        values.add(value);
      }
    }
    return String.join(" ", values);
  }

  private static String stripHtml(String value) {
    if (value == null || value.isBlank()) {
      return "";
    }
    return value.replaceAll("<[^>]+>", " ").replaceAll("\\s+", " ").trim();
  }

