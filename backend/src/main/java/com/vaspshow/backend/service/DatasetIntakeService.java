  /** Parse CIF cell parameters {a,b,c,alpha,beta,gamma}; null if any is missing/invalid. */
  private static double[] cifCell(String text) {
    Double a = cifNumber(text, "_cell_length_a");
    Double b = cifNumber(text, "_cell_length_b");
    Double c = cifNumber(text, "_cell_length_c");
    Double alpha = cifNumber(text, "_cell_angle_alpha");
    Double beta = cifNumber(text, "_cell_angle_beta");
    Double gamma = cifNumber(text, "_cell_angle_gamma");
    if (a == null || b == null || c == null || alpha == null || beta == null || gamma == null) {
      return null;
    }
    if (a <= 0 || b <= 0 || c <= 0) {
      return null;
    }
    return new double[] {a, b, c, alpha, beta, gamma};
  }

  private static Double cifNumber(String text, String tag) {
    for (String raw : text.split("\\r?\\n")) {
      String line = raw.trim();
      if (line.startsWith(tag)
          && (line.length() == tag.length() || Character.isWhitespace(line.charAt(tag.length())))) {
        String rest = line.substring(tag.length()).replaceAll("\\(.*?\\)", "").trim();
        String first = rest.split("\\s+")[0];
        try {
          return Double.parseDouble(first);
        } catch (NumberFormatException ex) {
          return null;
        }
      }
    }
    return null;
  }

  /** Build the cartesian lattice matrix (rows = a,b,c vectors in Å) from cell parameters. */
  private static double[][] cellMatrix(double[] cell) {
    double a = cell[0];
    double b = cell[1];
    double c = cell[2];
    double alpha = Math.toRadians(cell[3]);
    double beta = Math.toRadians(cell[4]);
    double gamma = Math.toRadians(cell[5]);
    double cosA = Math.cos(alpha);
    double cosB = Math.cos(beta);
    double cosG = Math.cos(gamma);
    double sinG = Math.sin(gamma);
    double bx = b * cosG;
    double by = b * sinG;
    double cx = c * cosB;
    double cy = sinG == 0 ? 0 : c * (cosA - cosB * cosG) / sinG;
    double volume = 1 - cosA * cosA - cosB * cosB - cosG * cosG + 2 * cosA * cosB * cosG;
    double cz = sinG == 0 ? 0 : c * Math.sqrt(Math.max(volume, 0)) / sinG;
    return new double[][] {
        {round6(a), 0.0, 0.0},
        {round6(bx), round6(by), 0.0},
        {round6(cx), round6(cy), round6(cz)}
    };
  }

  private static List<List<Double>> latticeRows(double[][] matrix) {
    List<List<Double>> rows = new ArrayList<>();
    for (double[] row : matrix) {
      rows.add(List.of(row[0], row[1], row[2]));
    }
    return rows;
  }

  /** Locate position/force column offsets from an extended-XYZ {@code Properties=} spec. */
  private static int[] xyzLayout(String comment) {
    int posOffset = 1; // default simple xyz: element then x y z
    int forceOffset = -1;
    if (comment != null) {
      java.util.regex.Matcher m = java.util.regex.Pattern
          .compile("(?i)Properties\\s*=\\s*(?:\"([^\"]*)\"|(\\S+))").matcher(comment);
      if (m.find()) {
        String spec = m.group(1) != null ? m.group(1) : m.group(2);
        String[] tokens = spec.split(":");
        int cursor = 0;
        for (int t = 0; t + 2 < tokens.length; t += 3) {
          String name = tokens[t].toLowerCase(Locale.ROOT);
          int count;
          try {
            count = Integer.parseInt(tokens[t + 2]);
          } catch (NumberFormatException ex) {
            count = 1;
          }
          if (name.equals("pos") || name.equals("positions") || name.equals("r")) {
            posOffset = cursor;
          } else if (name.equals("forces") || name.equals("force") || name.equals("f")) {
            forceOffset = cursor;
          }
          cursor += count;
        }
      }
    }
    return new int[] {posOffset, forceOffset};
  }

  private static double[][] parseXyzLattice(String comment) {
    String latStr = extractQuoted(comment, "Lattice");
    if (latStr.isBlank()) {
      return null;
    }
    String[] p = latStr.trim().split("\\s+");
    if (p.length < 9) {
      return null;
    }
    try {
      double[] v = new double[9];
      for (int k = 0; k < 9; k += 1) {
        v[k] = round6(Double.parseDouble(p[k]));
      }
      return new double[][] {{v[0], v[1], v[2]}, {v[3], v[4], v[5]}, {v[6], v[7], v[8]}};
    } catch (NumberFormatException ex) {
      return null;
    }
  }

