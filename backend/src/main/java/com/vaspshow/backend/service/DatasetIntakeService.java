  private DatasetSubmissionResponse runProfile(long id, boolean strict) {
    DatasetSubmissionResponse submission = findById(id);
    if (!"APPROVED".equals(submission.status())) {
      if (strict) {
        throw new ApiException(HttpStatus.BAD_REQUEST, "仅已批准来源可进行自动解析预检");
      }
      return submission;
    }
    Map<String, Object> profile = new LinkedHashMap<>();
    profile.put("parsedAt", Instant.now().toString());
    String stage;
    String message;
    try {
      DownloadSample sample = downloadSample(submission.dataUrl());
      profile.put("fileName", sample.fileName());
      profile.put("sizeBytes", sample.totalSize());
      profile.put("sampleBytes", sample.bytes().length);
      profile.put("sampled", sample.truncated());
      String format = detectSampleFormat(sample);
      profile.put("format", format);
      parseSample(profile, format, sample);
      stage = "VALIDATED";
      Object summary = profile.getOrDefault("summary", "已提取字段与结构样本");
      message = "自动解析预检完成：格式 " + format + "；" + summary
          + "。仍需人工确认字段映射/单位后才能入库发布。";
    } catch (Exception ex) {
      profile.put("status", "failed");
      profile.put("warning", ex.getMessage() == null ? ex.getClass().getSimpleName() : ex.getMessage());
      stage = "ADAPTER_REQUIRED";
      message = "自动解析预检未完成：" + profile.get("warning") + "。可人工下载样本后再配置适配器。";
    }
    return applyProfile(id, stage, message, toJson(profile));
  }

