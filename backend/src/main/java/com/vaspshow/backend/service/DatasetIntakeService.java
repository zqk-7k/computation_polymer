  public IngestSuggestionResponse ingestSuggestion(String authorization, long id) {
    authService.requireDatasetReviewAccess(authorization);
    DatasetSubmissionResponse submission = findById(id);
    String datasetKey = "intake_" + id;
    String datasetName = submission.datasetName() == null || submission.datasetName().isBlank()
        ? "自动入库数据集 #" + id
        : submission.datasetName();
    String format = "";
    List<String> fields = new ArrayList<>();
    try {
      if (submission.parseProfile() != null && !submission.parseProfile().isBlank()) {
        JsonNode profile = objectMapper.readTree(submission.parseProfile());
        format = profile.path("format").asText("");
        JsonNode fieldsNode = profile.path("recordFields").isArray()
            ? profile.path("recordFields")
            : profile.path("fields");
        if (fieldsNode.isArray()) {
          fieldsNode.forEach(node -> fields.add(node.asText("")));
        }
      }
    } catch (Exception ignored) {
      // No usable parse profile; the suggestion is simply empty.
    }
    boolean tabular = isTabularFormat(format);
    boolean structure = isStructureFormat(format);
    boolean supported = tabular || structure;
    Map<String, String> suggested = tabular ? suggestMapping(fields) : Map.of();
    String note;
    if (structure) {
      note = "结构格式 " + format + " 可直接入库：结构适配器将解析元素/坐标/化学式（extxyz 另取 energy）并写入展示库数据集 "
          + datasetKey + "，无需字段映射；前若干条结构，仍需人工核对。";
    } else if (tabular) {
      note = "已按字段名建议映射，可编辑后确认入库；将写入展示库数据集 " + datasetKey + "（前若干条，需人工确认）。";
    } else {
      note = "当前解析格式为 " + (format.isBlank() ? "未知" : format)
          + "，支持 CSV/TSV/JSON/JSONL 表格与 XYZ/EXTXYZ/CIF/POSCAR/HDF5 结构来源直接入库；其它格式需人工适配器。";
    }
    return new IngestSuggestionResponse(datasetKey, datasetName, supported, format, fields, suggested, note);
  }

  public DatasetSubmissionResponse ingestSubmission(String authorization, long id, IngestRequest request) {
    authService.requireDatasetReviewAccess(authorization);
    DatasetSubmissionResponse submission = findById(id);
    if (!"APPROVED".equals(submission.status())) {
      throw new ApiException(HttpStatus.BAD_REQUEST, "仅已批准来源可入库");
    }
    int limit = request != null && request.limit() != null
        ? Math.max(1, Math.min(request.limit(), 1000))
        : 200;
    String datasetKey = "intake_" + id;
    String datasetName = request != null && request.datasetName() != null && !request.datasetName().isBlank()
        ? request.datasetName().trim()
        : (submission.datasetName() == null || submission.datasetName().isBlank() ? "自动入库数据集 #" + id : submission.datasetName());

    List<Map<String, String>> rows;
    String modeNote;
    try {
      DownloadSample sample = downloadSample(submission.dataUrl());
      String format = detectSampleFormat(sample);
      if (isStructureFormat(format)) {
        rows = extractStructureRecords(format, sample, limit, datasetKey, datasetName);
        modeNote = "结构适配器（" + format + "）解析坐标/元素/化学式";
      } else if (isTabularFormat(format)) {
        Map<String, String> mapping = request == null || request.mapping() == null ? Map.of() : request.mapping();
        if (mapping.isEmpty()) {
          throw new ApiException(HttpStatus.BAD_REQUEST, "请先确认字段映射");
        }
        rows = mapTabularRecords(extractTabularRecords(format, sample, limit), mapping, datasetKey, datasetName);
        modeNote = "表格来源（" + format + "）按确认的字段映射";
      } else {
        throw new ApiException(HttpStatus.BAD_REQUEST,
            "仅支持 CSV/TSV/JSON/JSONL 表格或 XYZ/EXTXYZ/CIF/POSCAR/HDF5 结构来源入库，当前为 " + format);
      }
    } catch (ApiException ex) {
      throw ex;
    } catch (Exception ex) {
      throw new ApiException(HttpStatus.BAD_REQUEST,
          "读取来源数据失败: " + (ex.getMessage() == null ? ex.getClass().getSimpleName() : ex.getMessage()));
    }
    if (rows.isEmpty()) {
      throw new ApiException(HttpStatus.BAD_REQUEST, "来源未解析出可入库的记录");
    }

    int written = displayDatasetService.ingestRecords(datasetKey, datasetName, rows);
    String message = "已半自动入库 " + written + " 条记录到展示库数据集 " + datasetKey
        + "（" + modeNote + "，写入前 " + written + " 条）。可在数据中心查看，后续仍需人工核对单位与完整性。";
    return applyStage(id, "PUBLISHED", message);
  }

  /** Withdraw a published ingest: remove its rows from the display DB and reset the stage. */
  public DatasetSubmissionResponse withdrawIngest(String authorization, long id) {
    authService.requireDatasetReviewAccess(authorization);
    findById(id); // 404 if the submission does not exist
    int removed = displayDatasetService.deleteDataset("intake_" + id);
    return applyStage(id, "VALIDATED",
        "已撤回入库：从展示库移除 " + removed + " 条记录，可重新入库。");
  }

  private DatasetSubmissionResponse applyStage(long id, String stage, String message) {
    String now = Instant.now().toString();
    try (Connection connection = openConnection();
         PreparedStatement statement = connection.prepareStatement("""
             UPDATE DATASET_SUBMISSIONS
             SET PIPELINE_STAGE = ?, PIPELINE_MESSAGE = ?, UPDATED_AT = ?
             WHERE ID = ?
             """)) {
      statement.setString(1, stage);
      statement.setString(2, limitText(message, 1000));
      statement.setString(3, now);
      statement.setLong(4, id);
      statement.executeUpdate();
      return findById(id);
    } catch (SQLException ex) {
      throw databaseError(ex);
    }
  }

  private Map<String, String> suggestMapping(List<String> fields) {
    Map<String, String> mapping = new LinkedHashMap<>();
    for (String field : fields) {
      if (field == null || field.isBlank()) {
        continue;
      }
      String lower = field.toLowerCase(Locale.ROOT);
      putIfAbsentMatch(mapping, "SMILES", field, lower.contains("smiles"));
      putIfAbsentMatch(mapping, "MATERIAL_ID", field,
          lower.equals("id") || lower.contains("material_id") || lower.contains("mpid")
              || lower.contains("refcode") || lower.contains("identifier"));
      putIfAbsentMatch(mapping, "MATERIAL_NAME", field, lower.contains("name") || lower.contains("title"));
      putIfAbsentMatch(mapping, "COMPOSITION", field, lower.contains("formula") || lower.contains("composition"));
      putIfAbsentMatch(mapping, "ATOM_COUNT", field,
          lower.contains("natoms") || lower.equals("atoms") || lower.contains("atom_count") || lower.contains("n_atoms"));
      putIfAbsentMatch(mapping, "HOMO_LUMO_GAP", field, lower.contains("gap"));
      putIfAbsentMatch(mapping, "HOMO", field, lower.contains("homo") && !lower.contains("gap"));
      putIfAbsentMatch(mapping, "LUMO", field, lower.contains("lumo"));
      putIfAbsentMatch(mapping, "ENERGY", field,
          lower.contains("energy") && !lower.contains("homo") && !lower.contains("lumo") && !lower.contains("gap"));
      putIfAbsentMatch(mapping, "CHARGE", field, lower.contains("charge"));
      putIfAbsentMatch(mapping, "SPIN", field, lower.contains("spin") || lower.contains("multiplic"));
      putIfAbsentMatch(mapping, "CALCULATION_SOFTWARE", field,
          lower.contains("software") || lower.contains("functional") || lower.contains("method"));
      putIfAbsentMatch(mapping, "DOI", field, lower.contains("doi"));
    }
    return mapping;
  }

  private static void putIfAbsentMatch(Map<String, String> mapping, String target, String field, boolean match) {
    if (match && !mapping.containsKey(target)) {
      mapping.put(target, field);
    }
  }

  private List<Map<String, String>> extractTabularRecords(String format, DownloadSample sample, int limit) throws Exception {
    List<Map<String, String>> records = new ArrayList<>();
    if ("JSON".equals(format)) {
      JsonNode root = objectMapper.readTree(sample.bytes());
      JsonNode array = root.isArray() ? root
          : (root.path("data").isArray() ? root.path("data")
          : (root.path("records").isArray() ? root.path("records") : null));
      if (array != null) {
        for (JsonNode item : array) {
          if (records.size() >= limit) {
            break;
          }
          if (item.isObject()) {
            records.add(jsonObjectToRow(item));
          }
        }
      } else if (root.isObject()) {
        records.add(jsonObjectToRow(root));
      }
    } else if ("JSONL".equals(format)) {
      String[] lines = new String(sample.bytes(), StandardCharsets.UTF_8).split("\\r?\\n");
      for (String line : lines) {
        if (records.size() >= limit) {
          break;
        }
        if (line.isBlank()) {
          continue;
        }
        JsonNode node = objectMapper.readTree(line);
        if (node.isObject()) {
          records.add(jsonObjectToRow(node));
        }
      }
    } else if ("CSV".equals(format) || "TSV".equals(format)) {
      String[] lines = new String(sample.bytes(), StandardCharsets.UTF_8).split("\\r?\\n");
      if (lines.length >= 2) {
        String header = lines[0];
        String delimiter = "TSV".equals(format) ? "\t" : (header.contains("\t") ? "\t" : (header.contains(";") ? ";" : ","));
        List<String> columns = new ArrayList<>();
        for (String col : header.split(java.util.regex.Pattern.quote(delimiter))) {
          columns.add(col.trim().replaceAll("^\"|\"$", ""));
        }
        for (int i = 1; i < lines.length && records.size() < limit; i += 1) {
          if (lines[i].isBlank()) {
            continue;
          }
          String[] cells = lines[i].split(java.util.regex.Pattern.quote(delimiter));
          Map<String, String> row = new LinkedHashMap<>();
          for (int c = 0; c < columns.size() && c < cells.length; c += 1) {
            row.put(columns.get(c), cells[c].trim().replaceAll("^\"|\"$", ""));
          }
          records.add(row);
        }
      }
    }
    return records;
  }

  private static Map<String, String> jsonObjectToRow(JsonNode object) {
    Map<String, String> row = new LinkedHashMap<>();
    object.fields().forEachRemaining(entry -> {
      JsonNode value = entry.getValue();
      row.put(entry.getKey(), value.isValueNode() ? value.asText("") : value.toString());
    });
    return row;
  }

