  private DatasetSubmissionResponse insertSubmission(String submittedBy, DatasetSubmissionRequest request) {
    String datasetName = optional(request == null ? null : request.datasetName(), 180);
    String dataType = optional(request == null ? null : request.dataType(), 80);
    String description = required(request == null ? null : request.description(), "数据集说明", 2000);
    String paperUrl = optionalUrl(request == null ? null : request.paperUrl(), "论文链接");
    String dataUrl = optionalUrl(request == null ? null : request.dataUrl(), "数据下载或入口链接");
    if (paperUrl.isBlank() && dataUrl.isBlank()) {
      throw new ApiException(HttpStatus.BAD_REQUEST, "请至少填写一个论文链接或数据链接");
    }
    String dataFormat = optional(request == null ? null : request.dataFormat(), 80);
    String license = optional(request == null ? null : request.license(), 180);
    String providedFields = optional(request == null ? null : request.providedFields(), 1000);
    if (datasetName.isBlank()) {
      datasetName = "待命名数据集";
    }
    if (dataType.isBlank()) {
      dataType = "待分类";
    }
    if (dataFormat.isBlank()) {
      dataFormat = "待识别";
    }
    if (license.isBlank()) {
      license = "未说明";
    }
    if (providedFields.isBlank()) {
      providedFields = "待管理员核对";
    }
    String now = Instant.now().toString();
    String sql = """
        INSERT INTO DATASET_SUBMISSIONS (
          DATASET_NAME, DATA_TYPE, DESCRIPTION, PAPER_URL, DATA_URL, DATA_FORMAT,
          LICENSE_TEXT, PROVIDED_FIELDS, SUBMITTED_BY, SUBMITTED_AT, STATUS,
          REVIEW_NOTE, REVIEWED_AT, PIPELINE_STAGE, PIPELINE_MESSAGE, UPDATED_AT
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 'SUBMITTED', '', '', 'SUBMITTED',
          '已提交，等待超级管理员审核公开来源、许可证和字段可用性。', ?)
        """;
    try (Connection connection = openConnection();
         PreparedStatement statement = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
      statement.setString(1, datasetName);
      statement.setString(2, dataType);
      statement.setString(3, description);
      statement.setString(4, paperUrl);
      statement.setString(5, dataUrl);
      statement.setString(6, dataFormat);
      statement.setString(7, license);
      statement.setString(8, providedFields);
      statement.setString(9, submittedBy);
      statement.setString(10, now);
      statement.setString(11, now);
      statement.executeUpdate();
      try (ResultSet generated = statement.getGeneratedKeys()) {
        generated.next();
        return findById(generated.getLong(1));
      }
    } catch (SQLException ex) {
      throw databaseError(ex);
    }
  }

  public List<DatasetSubmissionResponse> listSubmissions(String authorization) {
    AuthUserResponse user = authService.requireDatasetSubmissionAccess(authorization);
    boolean reviewAccess = AuthService.ROLE_SUPER_ADMIN.equals(user.role());
    String sql = reviewAccess
        ? "SELECT * FROM DATASET_SUBMISSIONS ORDER BY UPDATED_AT DESC, ID DESC"
        : "SELECT * FROM DATASET_SUBMISSIONS WHERE SUBMITTED_BY = ? ORDER BY UPDATED_AT DESC, ID DESC";
    try (Connection connection = openConnection();
         PreparedStatement statement = connection.prepareStatement(sql)) {
      if (!reviewAccess) {
        statement.setString(1, user.username());
      }
      try (ResultSet rs = statement.executeQuery()) {
        List<DatasetSubmissionResponse> submissions = new ArrayList<>();
        while (rs.next()) {
          submissions.add(fromResult(rs));
        }
        return submissions;
      }
    } catch (SQLException ex) {
      throw databaseError(ex);
    }
  }

