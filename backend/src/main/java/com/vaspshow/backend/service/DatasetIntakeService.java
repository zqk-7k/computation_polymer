  private String required(String value, String label, int maxLength) {
    String text = optional(value, maxLength);
    if (text.isBlank()) {
      throw new ApiException(HttpStatus.BAD_REQUEST, label + "不能为空");
    }
    return text;
  }

  private String optional(String value, int maxLength) {
    String text = value == null ? "" : value.trim();
    if (text.length() > maxLength) {
      throw new ApiException(HttpStatus.BAD_REQUEST, "输入内容过长");
    }
    return text;
  }

  private String optionalUrl(String value, String label) {
    String url = optional(value, 500);
    if (url.isBlank()) {
      return "";
    }
    try {
      URI uri = URI.create(url);
      if (!"https".equalsIgnoreCase(uri.getScheme()) && !"http".equalsIgnoreCase(uri.getScheme())) {
        throw new IllegalArgumentException();
      }
    } catch (IllegalArgumentException ex) {
      throw new ApiException(HttpStatus.BAD_REQUEST, label + "必须为 http/https 地址");
    }
    return url;
  }

  private Connection openConnection() throws SQLException {
    return DriverManager.getConnection(resolveJdbcUrl(), "sa", "");
  }

  private String resolveJdbcUrl() {
    String cached = jdbcUrl;
    if (cached != null) {
      return cached;
    }
    synchronized (this) {
      if (jdbcUrl == null) {
        Path path = Paths.get(properties.getDbPath());
        Path cwd = Paths.get("").toAbsolutePath();
        Path candidate = path.isAbsolute() ? path : cwd.resolve(path).normalize();
        Path projectCandidate = path.isAbsolute() ? path : cwd.resolve("..").resolve(path).normalize();
        if (!path.isAbsolute() && Files.isDirectory(projectCandidate.getParent())) {
          candidate = projectCandidate;
        }
        try {
          Files.createDirectories(candidate.toAbsolutePath().getParent());
        } catch (Exception ex) {
          throw new IllegalStateException("创建数据接入数据库目录失败: " + ex.getMessage(), ex);
        }
        String normalized = candidate.toAbsolutePath().normalize().toString().replace('\\', '/');
        jdbcUrl = "jdbc:h2:" + normalized;
      }
      return jdbcUrl;
    }
  }

  private static ApiException databaseError(SQLException ex) {
    return new ApiException(HttpStatus.INTERNAL_SERVER_ERROR, "读取数据接入数据库失败: " + ex.getMessage());
  }
