  private static String fileNameFromUrl(String url) {
    try {
      String path = URI.create(url).getPath();
      String[] segments = path.split("/");
      // Repositories like Zenodo end file URLs with .../files/<name>/content,
      // so skip generic trailing segments to recover the real filename.
      for (int i = segments.length - 1; i >= 0; i -= 1) {
        String segment = segments[i];
        if (segment.isBlank() || "content".equals(segment) || "download".equals(segment)) {
          continue;
        }
        return segment;
      }
      return "";
    } catch (Exception ex) {
      return "";
    }
  }

